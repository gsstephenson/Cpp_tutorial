#include <iostream>

int main() {
    int a = 5;
    double b = 10;
    int sum = a + b;
    int difference = a - b;
    int product = a * b;
    double quotient = a / b;

    std::cout << "Sum: " << sum << "\n";
    std::cout << "Difference: " << difference << "\n";
    std::cout << "Product: " << product << "\n";
    std::cout << "Quotient: " << quotient << "\n";
    
    return 0;
}