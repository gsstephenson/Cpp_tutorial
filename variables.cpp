// Format : <datatype> <name> = <value>;
#include <iostream>
#include <string>

int main() {
    int age = 25;
    double gpa = 4.0;
    char grade = 'A';
    bool isStudent = false;
    std::string name = "George";

    std::cout << age << "\n";
    std::cout << gpa << "\n";
    std::cout << grade << "\n";
    std::cout << isStudent << "\n";
    std::cout << name << "\n";
}